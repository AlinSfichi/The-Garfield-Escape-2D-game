package PaooGame.States;
/**
 * Enum pentru numele stărilor jocului.
 * Acest enum definește toate stările posibile pe care le poate avea jocul.
 */
public enum StatesNames {
    GameOverState,   // Stare pentru sfârșitul jocului
    GameWinState,    // Stare pentru câștigarea jocului
    HelpState,       // Stare pentru ecranul de ajutor
    HighScoresState, // Stare pentru ecranul cu scoruri
    MenuState,       // Stare pentru meniul principal
    PauseState,      // Stare pentru pauză
    PlayState,       // Stare pentru jocul în desfășurare
    SettingsState    // Stare pentru setările jocului
}
